package org.example.ws.model;

public class Greeting {

	private int id;
	private String text;
	
	
	public Greeting() {
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
	
}
