package com.mediAssist.model;

public class Region {

	private Double regionId;
	private String regionName;
	private Double visibleInApp;
	
	public Double getRegionId() {
		return regionId;
	}
	public void setRegionId(Double regionId) {
		this.regionId = regionId;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public Double getVisibleInApp() {
		return visibleInApp;
	}
	public void setVisibleInApp(Double visibleInApp) {
		this.visibleInApp = visibleInApp;
	}
	
	
}
