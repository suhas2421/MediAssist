package com.mediAssist.model;

public class Scope {

	private Double scopeId;
	private String scopeName;
	private Double scopeSeqNumber;
	
	
	public String getScopeName() {
		return scopeName;
	}
	public void setScopeName(String scopeName) {
		this.scopeName = scopeName;
	}
	public Double getScopeId() {
		return scopeId;
	}
	public void setScopeId(Double scopeId) {
		this.scopeId = scopeId;
	}
	public Double getScopeSeqNumber() {
		return scopeSeqNumber;
	}
	public void setScopeSeqNumber(Double scopeSeqNumber) {
		this.scopeSeqNumber = scopeSeqNumber;
	}
	
	
}
